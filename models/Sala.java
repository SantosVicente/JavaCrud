public class Sala {
    private int id;
    private int numero;

    // Criando métodos para pegar/inserir dados:
    // Pegar dados:
    
    // Pegando ID da sala:
    public int getId(){
        return id;
    }

    // Pegando número da sala:
    public int getNum(){
        return numero;
    }

    // Inserir dados:
    public void setId(int id){
        this.id = id;
    }

    // Inserir Número da sala:
    public void setNum(int Num){
        this.numero = Num;
    }
}
